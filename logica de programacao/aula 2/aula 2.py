# operadores logicos

# operador and - and - quando todas as condiçoes sao verdadeiras

# operador or - or - quando apenas 1 as condiçoes sao verdadeiras

# operador not - not - quando ele inverte a condiçao da sentença

num1 = 34
num2 = 22
num3= 11

# operador AND

print(num1>num2 and num3>num1)

# operador OR

print(num1>num2 or num3>num1)

# operador not

print(num1<num2 or not num3<num1)

